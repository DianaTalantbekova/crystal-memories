class LevelInfo {
  final int id;
  final int matrixSize;
  final Duration duration;
  final int flickCount;
  final int coins;
  final int space;
  final int total;

  const LevelInfo({
    required this.id,
    required this.matrixSize,
    required this.duration,
    required this.flickCount,
    required this.coins,
    required this.space,
    required this.total,
  });
}